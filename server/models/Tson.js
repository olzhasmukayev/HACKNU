import mongoose from "mongoose";

const TsonSchema = new mongoose.Schema({
  role: {
    type: String,
    required: true,
  },
  login: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  code: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
});

const Tson = mongoose.model("Tson", TsonSchema);
export default Tson;
